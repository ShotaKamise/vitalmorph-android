    /** 名前の部分一致検索(空文字なら全件)。 */
    fun search(query: String, customFoods: List<FoodCatalogItem> = emptyList()): List<FoodCatalogItem> {
        val all = customFoods + builtin
        if (query.isBlank()) return all
        return all.filter { it.name.contains(query.trim(), ignoreCase = true) }
